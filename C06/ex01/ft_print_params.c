#include <unistd.h>

void	ft_putstr(char *str);

int	main(int ac, char *av[])
{
	int	n;

	n = 1;
	while (n < ac)
	{
		ft_putstr(&av[n][0]);
		n++;
	}
	return (0);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
	return ;
}
